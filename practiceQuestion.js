// 2. Data Types
// JavaScript has different data types to represent different kinds of values. Here are a few important ones:

// Numbers: Used for any number, like 5, 3.14, or -2.
// Strings: Text wrapped in quotes, like "hello" or 'world'.
// Booleans: true or false values.
// Null: Represents an intentional absence of any value.
// Undefined: A variable without an assigned value.
// Objects: Used to store collections of data.
// Arrays: Lists of values.
// Example:

// javascript
// Copy code
// let number = 42;
// let message = "Hello, world!";
// let isLearning = true;
// let emptyValue = null;
// let notAssigned; // this is undefined
// let person = { name: "Alice", age: 25 }; // an object
// let colors = ["red", "green", "blue"]; // an array
